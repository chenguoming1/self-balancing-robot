/* SR04 ECHO rising-edge callback (PB11) via EXTI15_10 */
void SR04_EXTI_Callback(void)
{
  delay_us(10);
  TIM4->CNT = 0;
  HAL_TIM_Base_Start(&htim4);
  while (HAL_GPIO_ReadPin(ECHO_GPIO_Port, ECHO_Pin) == GPIO_PIN_SET);
  HAL_TIM_Base_Stop(&htim4);
  SR04_Distance = (float)(TIM4->CNT) * 1.7f;
}

/* Mode-specific peripheral initialisation */
void Mode_Init(void)
{
  OLED_Clear();
  oled_first_show();
  switch (CTRL_MODE) {
    case 97:
      SR04_Configuration();
      Timer4_Init(5000, 7199);
      break;
    case 98:
      uart2_init(9600);
      break;
    case 99:
      Tracking_Init();
      break;
    case 100:
      PS2_Init();
      PS2_SetInit();
      break;
  }
}

